let noiseOffset = 0;
let noiseStep = 0.005;

function generateTerrain() {
    noiseDetail(5)
	for (let x = 0; x < width; x++) {
		let y = noise(noiseOffset) * height / 2 + height / 2; // Use Perlin noise to determine y value
		terrain.push({x: Math.round(x), y: Math.round(y)});
		noiseOffset += noiseStep;
	  }
}

function preload() {
	lunarLander = loadImage("lunarLander.svg");
	flame = loadImage("flame.svg")
	bender = loadFont("bender_light-webfont.ttf");
}

function setup() {
	createCanvas(windowWidth, windowHeight);
	imageMode(CENTER);
	angleMode(DEGREES);
	xPos = width / 2;
	yPos = height * 0;
    //generateTerrain();
}

function draw() {
	background(0);
	{
		fill(0);
		stroke(255);
		strokeWeight(1);
		beginShape();
		for (let x = 0; x < terrain.length; x++) {
			vertex(terrain[x].x, terrain[x].y);
		}
		vertex(width, height);  // Bottom right corner
		vertex(0, height);  // Bottom left corner
		endShape(CLOSE);
	}
	//terrain
	
	{
		push();
		scale(0.5);
		translate(xPos, yPos);
		rotate(direction);
		image(lunarLander, 0, 0, 30, 30 * (lunarLander.height / lunarLander.width));
		imageMode(CORNERS);
		if (flameLength > 0) {
			image(flame, -5, 15 * (lunarLander.height / lunarLander.width) - 2, 5, 15 * (lunarLander.height / lunarLander.width) - 2 + flameLength + random() * 10);
		}
		pop();
	}
	//lander

	{
		push();
		scale(1);
		fill("white");
		textSize(20)
		textAlign(LEFT);
		textFont(bender);
		text("HORIZONTAL SPEED", width - 300, 100);

		fill("white");
		textSize(20)
		textAlign(RIGHT);
		textFont(bender);
		text(Math.round(xVel * 20), width - 50, 100);

		fill("white");
		textSize(20)
		textAlign(LEFT);
		textFont(bender);
		text("VERTICAL SPEED", width - 300, 150);

		fill("white");
		textSize(20)
		textAlign(RIGHT);
		textFont(bender);
		text(Math.round(yVel * 20), width - 50, 150);

		fill("white");
		textSize(20)
		textAlign(LEFT);
		textFont(bender);
		text("DIRECTION", width  - 300, 200);

		fill("white");
		textSize(20);
		textAlign(RIGHT);
		textFont(bender);
		text(Math.round(direction % 180), width - 50, 200);
		pop();

		push();
		translate(width - 250, 250);
		stroke("green");
		line(50, -25, 50, 25);
		rotate(direction);
		image(lunarLander, 0, 0, 50, 50 * (lunarLander.height / lunarLander.width));
		pop();

		push();
		translate(width - 200, 275);
		rotate(direction);
		if (abs(round(direction % 180)) <= 4) {
			stroke("green")
		} else if (abs(round(direction % 180)) <= 16) {
			stroke("yellow")
		} else {
			stroke("red")
		}
		line(0, 0, 0, -50)
		pop();
	}
	//info displays
	{
		scale(0.02)
		push();
		if (up) {
			xVel += 0.05 * sin(direction);
			yVel -= 0.05 * cos(direction);
			if (flameLength <= 10 * (flame.height / flame.width)) {
				flameLength += 4
			}
		}
		if (left) {
			if (direction > -90) {
				direction -= 2;
			}
		}
		if (right) {
			if (direction < 90) {
				direction += 2;
			}
		}
		xPos += xVel;
		yPos += yVel;
		yVel += 0.005;
		if (flameLength > 0) {
			flameLength -= 2;
		}
		pop();
	}
	//controls
}

function keyPressed() {
	if (keyCode == UP_ARROW) {
		up = true;
	}
	if (keyCode == LEFT_ARROW) {
		left = true;
	}
	if (keyCode == RIGHT_ARROW) {
		right = true;
	}
}

function keyReleased() {
	if (keyCode == UP_ARROW) {
		up = false;
	}
	if (keyCode == LEFT_ARROW) {
		left = false;
	}
	if (keyCode == RIGHT_ARROW) {
		right = false;
	}
}